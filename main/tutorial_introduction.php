                Oyunun başında aldığınız kırmızı iksirler otomatik olarak ilk kısayol tuşuna yerleştirilecektir. Oraya koyduğunuz şeyi, uygun düğmeye basarak hemen (ekipman penceresini açmadan) kullanabilirsiniz.</p>
