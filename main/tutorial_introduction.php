                Dövüşe başlamak için vahşi bir köpeğe tıklayın. Yukarıdaki açıklama size seviye 1 vahşi bir köpekle dövüştüğünüzü gösterir. Siz de seviye 1 olduğunuz için seviye olarak sizden önemli ölçüde yüksek olan düşmanlardan kaçının. Dövüşte, yok ettiğiniz köpeklere veya boşluk tuşuyla "Depo"ya tekrar tıklayın. Veya ekranın alt kısmındaki silah simgesinden "Otomatik Saldırı" işlevini seçebilirsiniz - bu durumda karakteriniz düşmana ölene kadar vuracaktır. Daha sonra bir sonraki düşmana geçmelisin.<br/><br/>
                Soldaki kırmızı sütunda kendi sağlığını takip edebilirsin. Tamamen bittiyse, karakteriniz ölür, ancak on saniye sonra yeniden canlandırılabilir. Artık dövüşmüyorsan, karaten yenilenir. Herhangi bir zamanda (mücadele sırasında bile) kırmızı içeceklerden birini içmek için "1" düğmesine basabilirsin (yenilenme).</p>
